export const getRandomSuggestionSets = (count: number = 2): SuggestionSet[] => {
  const shuffled = [...SUGGESTION_SETS].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getSuggestionSetMatchingPlaceholder = (placeholderSet: any): SuggestionSet | null => {
  return SUGGESTION_SETS.find(set => 
    set.lessonTopic.includes(placeholderSet.lessonTopic.split(' ')[0]) ||
    set.lessonFocus.includes(placeholderSet.lessonFocus.split(' ')[0])
  ) || null;
};
